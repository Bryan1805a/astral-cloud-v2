"use client";

import { useEffect, useState } from "react";
import { useRouter, usePathname } from "next/navigation";
import Link from "next/link";

interface User {
  id: string;
  username: string;
  email: string;
  role: string;
  balance: string;
}

const NAV_ITEMS = [
  { href: "/dashboard", label: "Overview", icon: HomeIcon },
  { href: "/dashboard/servers", label: "Servers", icon: ServerIcon },
  { href: "/dashboard/billing", label: "Billing", icon: BillingIcon },
  { href: "/dashboard/api-keys", label: "API Keys", icon: KeyIcon },
];

const ADMIN_ITEMS = [
  { href: "/dashboard/admin/plans", label: "Plans", icon: PlanIcon },
  { href: "/dashboard/admin/images", label: "Images", icon: ImageIcon },
  { href: "/dashboard/admin/nodes", label: "Nodes", icon: NodeIcon },
  { href: "/dashboard/admin/regions", label: "Regions", icon: RegionIcon },
  { href: "/dashboard/admin/users", label: "Users", icon: UsersIcon },
  { href: "/dashboard/admin/vouchers", label: "Vouchers", icon: VoucherIcon },
  { href: "/dashboard/admin/audit-logs", label: "Audit Logs", icon: LogIcon },
];

export default function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const router = useRouter();
  const pathname = usePathname();
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const token = localStorage.getItem("access_token");
    if (!token) {
      router.push("/login");
      return;
    }

    fetch("/api/auth/me", {
      headers: { Authorization: `Bearer ${token}` },
    })
      .then((res) => {
        if (!res.ok) throw new Error("Unauthorized");
        return res.json();
      })
      .then((json) => setUser(json.data))
      .catch(() => {
        localStorage.removeItem("access_token");
        router.push("/login");
      })
      .finally(() => setLoading(false));
  }, [router]);

  async function handleLogout() {
    const token = localStorage.getItem("access_token");
    if (token) {
      await fetch("/api/auth/logout", {
        method: "POST",
        headers: { Authorization: `Bearer ${token}` },
      });
    }
    localStorage.removeItem("access_token");
    router.push("/login");
  }

  if (loading) {
    return (
      <main className="flex min-h-screen items-center justify-center bg-gray-950">
        <p className="text-gray-400">Loading...</p>
      </main>
    );
  }

  return (
    <div className="flex min-h-screen bg-gray-950">
      <aside className="fixed inset-y-0 left-0 z-30 flex w-56 flex-col border-r border-gray-800 bg-gray-900">
        <div className="flex h-14 items-center border-b border-gray-800 px-4">
          <Link href="/dashboard" className="text-lg font-bold tracking-tight">
            Astral Cloud
          </Link>
        </div>

        <nav className="flex-1 space-y-1 px-3 py-4">
          {NAV_ITEMS.map((item) => {
            const isActive = pathname === item.href ||
              (item.href !== "/dashboard" && pathname.startsWith(item.href));
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                  isActive
                    ? "bg-gray-800 text-white"
                    : "text-gray-400 hover:bg-gray-800/50 hover:text-gray-200"
                }`}
              >
                <item.icon className="h-5 w-5" />
                {item.label}
              </Link>
            );
          })}

          {user && (user.role === "ADMIN" || user.role === "STAFF") && (
            <>
              <div className="pt-4">
                <p className="px-3 text-[11px] font-semibold uppercase tracking-wider text-gray-500">Admin</p>
              </div>
              {ADMIN_ITEMS.map((item) => {
                const isActive = pathname === item.href || pathname.startsWith(item.href);
                return (
                  <Link
                    key={item.href}
                    href={item.href}
                    className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                      isActive
                        ? "bg-gray-800 text-white"
                        : "text-gray-400 hover:bg-gray-800/50 hover:text-gray-200"
                    }`}
                  >
                    <item.icon className="h-5 w-5" />
                    {item.label}
                  </Link>
                );
              })}
            </>
          )}
        </nav>

        <div className="border-t border-gray-800 px-4 py-3">
          <div className="flex items-center justify-between">
            <div className="min-w-0">
              <p className="truncate text-sm font-medium text-gray-200">
                {user?.username}
              </p>
              <p className="truncate text-xs text-gray-400">${user?.balance}</p>
            </div>
            <button
              onClick={handleLogout}
              className="rounded-md p-1.5 text-gray-400 hover:bg-gray-800 hover:text-gray-200 transition-colors"
              title="Sign Out"
            >
              <LogoutIcon className="h-4 w-4" />
            </button>
          </div>
        </div>
      </aside>

      <main className="ml-56 flex-1 px-6 py-8">{children}</main>
    </div>
  );
}

